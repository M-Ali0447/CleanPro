// Get current language from localStorage or default
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'uz_latn';
}

// Set language
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang === 'uz_latn' ? 'uz' : lang === 'uz_cyrl' ? 'uz' : 'ru';
    return translations[lang] || translations.uz_latn;
}

// Get admin translations
function getAdminTranslations(lang) {
    return adminTranslations[lang] || adminTranslations.uz_latn;
}
